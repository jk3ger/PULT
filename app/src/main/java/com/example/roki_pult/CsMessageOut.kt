    fun beginQuery(cmd: Char, id: Int) {
        reset()
        usedBits = 0
        ptr = 1
        val commandPart = (cmd.code shl 4) and 0x70
        val idPart = id and 0x0F
        buffer[0] = (commandPart or idPart).toByte()
        buffer[1] = 0
    }

    fun hostBeginQuery(cmd: Char) {
        reset()
        usedBits = 0
        ptr = 1
        buffer[0] = (cmd.code and 0x7F).toByte()
        buffer[1] = 0
    }

    fun hostEnd() {
        end()
        if (ptr < buffer.size) {
            buffer[ptr] = '\n'.code.toByte()
            ptr++
        } else {
            Log.e("CsMessageOut", "Buffer overflow on hostEnd (newline)")
        }
        if (ptr < buffer.size) {
            buffer[ptr] = 0
        }
    }

    fun end() {
        if (usedBits > 0) {
            ptr++
        }
        if (ptr >= buffer.size - 1) {
            Log.e("CsMessageOut", "Buffer overflow on end()")
            return
        }
        val crc = crc8(buffer, ptr)
        buffer[ptr] = (crc.toInt() or 0x80).toByte()
        ptr++
        buffer[ptr] = 0
    }

    private fun crc8(data: ByteArray, length: Int): UByte {
        var crc: UByte = 0xFFu
        for (i in 0 until length) {
            val index = (crc xor (data[i].toUByte())).toInt()
            crc = Crc8Table[index]
        }
        return crc
    }

    fun getRawBuffer(): ByteArray {
        return buffer
    }

    fun getPayloadSize(): Int {
        return ptr
    }

    fun reset() {
        ptr = 0
        usedBits = 0
        buffer.fill(0)
    }

    fun addInt8(value: Int) {
        val currentByteAsInt = buffer[ptr].toInt() and 0xFF
        val firstPart = (value shl usedBits)
        buffer[ptr] = (currentByteAsInt or firstPart or 0x80).toByte()
        ptr++

        val bitsWritten = 7 - usedBits
        usedBits = 8 - bitsWritten

        buffer[ptr] = ((value shr bitsWritten) or 0x80).toByte()

        if (usedBits == 7) {
            ptr++
            if (ptr < buffer.size) {
                buffer[ptr] = 0
            }
            usedBits = 0
        }
    }

    fun addInt16(value: Int) {
        addInt8(value and 0xFF)
        addInt8((value shr 8) and 0xFF)
    }

    fun addInt32(value: Int) {
        addInt8(value and 0xFF)
        addInt8((value shr 8) and 0xFF)
        addInt8((value shr 16) and 0xFF)
        addInt8((value shr 24) and 0xFF)
    }

    fun addBytes(data: ByteArray) {
        for (byte in data) {
            addInt8(byte.toInt() and 0xFF)
        }
    }
}
